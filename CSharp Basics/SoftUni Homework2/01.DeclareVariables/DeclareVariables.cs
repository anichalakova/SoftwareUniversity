using System;

class DeclareVariables
{
    static void Main(string[] args)
    {
        ushort firstVariable = 52130; 
        sbyte secondVariable = -115; 
        int thirdVariable = 4825932; 
        byte fourthVariable = 97; 
        short fifthVariable = -10000;

        Console.WriteLine("Variable 1 is {0}", firstVariable); 
        Console.WriteLine("Variable 2 is {0}", secondVariable); 
        Console.WriteLine("Variable 3 is {0}", thirdVariable); 
        Console.WriteLine("Variable 4 is {0}", fourthVariable); 
        Console.WriteLine("Variable 5 is {0}", fifthVariable);
    }
}